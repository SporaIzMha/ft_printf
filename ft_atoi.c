#include "ft_printf.h"

int	ft_atoi(const char *str)
{
	unsigned long int	r;
	int					znak;
	unsigned int		i;

	r = 0;
	znak = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			znak = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	return ((int)(r * znak));
}
